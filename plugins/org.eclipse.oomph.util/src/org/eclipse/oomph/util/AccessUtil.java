  {
    Class<?> classBytesGeneratorClass = Class.forName(AccessUtil.class.getName().replace(AccessUtil.class.getSimpleName(), "ClassBytesGenerator"));
    classBytesGeneratorClass.getMethod("generate").invoke(null);
  }
